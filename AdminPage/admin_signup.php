<?php
require '../db.php';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = $_POST['username'];
    $email = $_POST['email'];
    $password = password_hash($_POST['password'], PASSWORD_DEFAULT); // Hash the password for security

    // Check if username or email already exists
    $check = $conn->prepare("SELECT * FROM Admins WHERE username=? OR email=?");
    $check->bind_param("ss", $username, $email);
    $check->execute();
    $result = $check->get_result();

    if ($result->num_rows > 0) {
        echo "Username or email already exists!";
    } else {
        // Insert admin into the Admins table
        $stmt = $conn->prepare("INSERT INTO Admins (username, email, password) VALUES (?, ?, ?)");
        $stmt->bind_param("sss", $username, $email, $password);
        if ($stmt->execute()) {
            echo "Admin account created successfully!";
            header("Location: admin_login.php"); // Redirect to the login page after sign-up
            exit;
        } else {
            echo "Error creating admin account.";
        }
    }
}
